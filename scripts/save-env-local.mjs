#!/usr/bin/env node
/**
 * Write Railway / production env JSON to gitignored .env.local.
 *
 * Usage:
 *   node scripts/save-env-local.mjs path/to/env.json
 *   cat env.json | node scripts/save-env-local.mjs --stdin
 *
 * Never logs secret values. Never commits .env.local.
 */
import fs from 'node:fs';
import path from 'node:path';

const OUTPUT = path.resolve(process.cwd(), '.env.local');
const REQUIRED_HINT_KEYS = ['OPENROUTER_API_KEY', 'VITE_OPENROUTER_KEY'];

function readInput() {
  const arg = process.argv[2];
  if (arg === '--stdin' || !arg) {
    const fd = fs.fstatSync(0);
    if (fd.isFIFO() || fd.isFile()) {
      return fs.readFileSync(0, 'utf8');
    }
    if (!arg) {
      throw new Error('Provide a JSON file path or pipe JSON on stdin.');
    }
  }
  const filePath = path.resolve(process.cwd(), arg);
  if (!fs.existsSync(filePath)) {
    throw new Error(`File not found: ${filePath}`);
  }
  return fs.readFileSync(filePath, 'utf8');
}

function normalizeEntries(parsed) {
  if (Array.isArray(parsed)) {
    return parsed
      .map((row) => {
        if (row && typeof row === 'object' && 'name' in row) {
          return [String(row.name), row.value ?? ''];
        }
        return null;
      })
      .filter(Boolean);
  }

  if (parsed && typeof parsed === 'object') {
    return Object.entries(parsed).map(([key, value]) => {
      if (value && typeof value === 'object' && 'value' in value) {
        return [key, value.value ?? ''];
      }
      return [key, value ?? ''];
    });
  }

  throw new Error('Expected JSON object or array of { name, value } entries.');
}

function escapeDotenvValue(value) {
  const str = value == null ? '' : String(value);
  if (str === '') return '';
  if (/[\s#"'\\]/.test(str) || str.startsWith('${')) {
    return `"${str.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n')}"`;
  }
  return str;
}

function main() {
  const raw = readInput().trim();
  if (!raw) {
    throw new Error('Empty input.');
  }

  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch (error) {
    throw new Error(`Invalid JSON: ${error instanceof Error ? error.message : String(error)}`);
  }

  const entries = normalizeEntries(parsed);
  if (entries.length === 0) {
    throw new Error('No env entries found in JSON.');
  }

  const lines = [
    '# Generated by scripts/save-env-local.mjs — DO NOT COMMIT',
    `# Updated: ${new Date().toISOString()}`,
    '',
  ];

  for (const [key, value] of entries.sort(([a], [b]) => a.localeCompare(b))) {
    if (!key || key.startsWith('#')) continue;
    lines.push(`${key}=${escapeDotenvValue(value)}`);
  }

  if (fs.existsSync(OUTPUT)) {
    const backupPath = `${OUTPUT}.bak`;
    fs.copyFileSync(OUTPUT, backupPath);
    process.stdout.write(`[save-env-local] Backed up existing file to ${backupPath}\n`);
  }

  fs.writeFileSync(OUTPUT, `${lines.join('\n')}\n`, { mode: 0o600 });

  const keys = entries.map(([key]) => key);
  const hasOpenRouter =
    keys.includes('OPENROUTER_API_KEY') || keys.includes('VITE_OPENROUTER_KEY');

  process.stdout.write(`[save-env-local] Wrote ${entries.length} variables to ${OUTPUT}\n`);
  process.stdout.write(`[save-env-local] Keys: ${keys.join(', ')}\n`);
  if (!hasOpenRouter) {
    process.stdout.write(
      `[save-env-local] Warning: missing OpenRouter key (${REQUIRED_HINT_KEYS.join(' or ')})\n`,
    );
  }
}

try {
  main();
} catch (error) {
  process.stderr.write(
    `[save-env-local] ${error instanceof Error ? error.message : String(error)}\n`,
  );
  process.exit(1);
}
