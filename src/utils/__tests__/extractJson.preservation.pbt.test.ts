/**
 * Preservation Property Tests — Clean JSON Parses Directly and Garbage Returns Null
 *
 * These tests verify behaviors that MUST be preserved after the fix is implemented:
 * 1. Clean JSON (no fences, no prose) parses correctly through extractJson
 * 2. Strings with no JSON structure return null
 * 3. validateVisualPlan works correctly with pre-parsed objects
 *
 * EXPECTED: These tests PASS on unfixed code (they confirm baseline behavior).
 *
 * Validates: Requirements 3.4, 3.5, 3.6
 */
import { describe, it, expect } from 'vitest';
import * as fc from 'fast-check';
import { extractJson } from '../extractJson';
import { validateVisualPlan } from '../../services/llmVisualDirector';

// ---------------------------------------------------------------------------
// Property 1: Clean JSON Preservation
// ---------------------------------------------------------------------------

describe('Preservation: Clean JSON Parses Directly', () => {
  /**
   * **Validates: Requirements 3.4**
   *
   * Property: For all valid JSON objects generated by fc.jsonValue(),
   * extractJson(JSON.stringify(obj)) deep-equals obj.
   * Clean JSON passes through unchanged — no fences, no prose, just valid JSON.
   *
   * Note: We use fc.dictionary with safe value types to avoid the -0/+0
   * serialization edge case (JSON.stringify(-0) === "0", JSON.parse("0") === 0).
   */
  it('extractJson returns parsed object for all valid JSON objects', () => {
    // Generate JSON objects that round-trip cleanly through JSON.stringify/parse
    const safeJsonObj = fc.dictionary(
      fc.string({ minLength: 1, maxLength: 10 }),
      fc.oneof(
        fc.integer({ min: -1000, max: 1000 }),
        fc.string({ maxLength: 20 }),
        fc.boolean(),
        fc.constant(null),
        fc.array(fc.oneof(fc.integer(), fc.string({ maxLength: 10 }), fc.boolean()), { maxLength: 3 }),
      ),
      { minKeys: 1, maxKeys: 5 },
    );

    fc.assert(
      fc.property(
        safeJsonObj,
        (obj) => {
          const jsonStr = JSON.stringify(obj);
          const result = extractJson(jsonStr);
          expect(result).toEqual(JSON.parse(jsonStr));
        },
      ),
      { numRuns: 200 },
    );
  });

  /**
   * **Validates: Requirements 3.4**
   *
   * Property: For valid JSON arrays, extractJson returns the parsed array.
   */
  it('extractJson returns parsed array for valid JSON arrays', () => {
    const safeJsonArr = fc.array(
      fc.oneof(
        fc.integer({ min: -1000, max: 1000 }),
        fc.string({ maxLength: 20 }),
        fc.boolean(),
        fc.constant(null),
      ),
      { minLength: 1, maxLength: 5 },
    );

    fc.assert(
      fc.property(
        safeJsonArr,
        (arr) => {
          const jsonStr = JSON.stringify(arr);
          const result = extractJson(jsonStr);
          expect(result).toEqual(arr);
        },
      ),
      { numRuns: 100 },
    );
  });
});

